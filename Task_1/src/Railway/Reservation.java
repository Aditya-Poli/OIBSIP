    public void Connect() {
        try{
            try {
                Class.forName("com.mysql.jdbc.Driver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AddTrain.class.getName()).log(Level.SEVERE, null, ex);
            }
            con = DriverManager.getConnection("jdbc:mysql://localhost/ors", System.getenv("USERNAME"), System.getenv("PASSWORD"));
        } catch (SQLException s) {
            s.printStackTrace();
        }
    }
    
    
    public void loadStartPlace() {
        
        try {
            pst = con.prepareStatement("SELECT startPlace FROM TrainDetails");
            rs = pst.executeQuery();
            textstartplace.removeAllItems();
            while(rs.next()){
                textstartplace.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Reservation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void loadEndPlace() {
        
        try {
            pst = con.prepareStatement("SELECT destinationPlace FROM TrainDetails");
            rs = pst.executeQuery();
            textendplace.removeAllItems();
            while(rs.next()){
                textendplace.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Reservation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void loadTrainDetails() {
        
        String startPlace = textstartplace.getSelectedItem().toString();
        String endPlace = textendplace.getSelectedItem().toString();
        
        try {
            pst = con.prepareStatement("SELECT * FROM TrainDetails WHERE startPlace = ? and destinationPlace = ?");
            pst.setString(1, startPlace);
            pst.setString(2, endPlace);
            rs = pst.executeQuery();
            
            if(rs.next() == true) {
                texttno.setText(rs.getString(1));
                texttname.setText(rs.getString(2));
                textprice.setText(rs.getString(5));
            } else {
                JOptionPane.showMessageDialog(this, "No Trains Available On This Route");
            }
            
        } catch (SQLException sqlex){
            Logger.getLogger(Reservation.class.getName()).log(Level.SEVERE, null, sqlex);
        }
    }

