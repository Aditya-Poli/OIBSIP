    private void textnotKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textnotKeyPressed
        // TODO add your handling code here:
        
        int price, numTickets, totalPrice;
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            
            price = Integer.parseInt(textprice.getText());
            numTickets = Integer.parseInt(textnot.getText());
            
            totalPrice = numTickets * price;
            
            texttotal.setText(String.valueOf(totalPrice));
        }
    }//GEN-LAST:event_textnotKeyPressed

