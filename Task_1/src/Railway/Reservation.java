    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        /*
        -- Table to store reservation details
        CREATE TABLE ReservationDetails (
            pno INT PRIMARY KEY,
            startPlace VARCHAR(255),
            endPlace varchar(255),
            tno INT,
            tname VARCHAR(255),
            price INT,
            date VARCHAR(255),
            tickets INT,
            total INT
        );
        */
        String pno = textpno.getText();
        String startPlace = textstartplace.getSelectedItem().toString();
        String endPlace = textendplace.getSelectedItem().toString();
        String tno = texttno.getText();
        String tname = texttname.getText();
        String price = textprice.getText();
        SimpleDateFormat date_form = new SimpleDateFormat("yyyy-mm-dd");
        String date = date_form.format(textdate.getDate());
        String tickets = textnot.getText();
        String total = texttotal.getText();
        
        
        try {
            pst = con.prepareStatement("INSERT INTO ReservationDetails(pno,startPlace,endPlace, tno, tname,price, date, tickets, total) VALUES(?,?,?,?,?,?,?,?,?)");
            pst.setString(1, pno);
            pst.setString(2, startPlace);
            pst.setString(3, endPlace);
            pst.setString(4, tno);
            pst.setString(5, tname);
            pst.setString(6, price);
            pst.setString(7, date);
            pst.setString(8, tickets);
            pst.setString(9, total);
            
            int k = pst.executeUpdate();
            
            if(k == 1){
                JOptionPane.showMessageDialog(this, "Ticket Booked Successfully.");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to Book Reservation.");
            }
            
            textpno.setText("");
            texttno.setText("");
            texttname.setText("");
            textprice.setText("");
            textdate.cleanup();
            textnot.setText("");
            texttotal.setText("");
            
            loadTrainDetails();
            
        } catch (SQLException ex) {
            Logger.getLogger(AddTrain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

